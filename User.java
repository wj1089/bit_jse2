package com.jse.swing;

public class User {
	
	private String name;
	private int height;
	
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setHeight(int height) {
		this. height = height;
	}
	public int getHeight() {
		return height;
	}
	
}
